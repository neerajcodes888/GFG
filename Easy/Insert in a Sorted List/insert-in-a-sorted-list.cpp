class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
          Node* tmp=new Node(data);
       //attach node at left
       if(!head) return tmp;
       if(data<head->data){
           tmp->next=head;
           return tmp;
       }
       
       //attach node at  middle of the list
       Node* curr=head;
       Node* nex=head->next;
       while(nex){
           if(curr->data<=data && data<=nex->data){
               curr->next=tmp;
               tmp->next=nex;
               return head;
           }
           curr=nex;
           nex=nex->next;
       }
       
       //attach node at end
       curr->next=tmp;
       return head;
    }
};


