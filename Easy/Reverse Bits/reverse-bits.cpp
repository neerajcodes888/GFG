class Solution {
  public:
    long long reversedBits(long long x) {
              int a=0;
        long long ans=0;
        while(x){
            
            if(x%2!=0){
                ans+=pow(2,32-1-a);
            }
            x/=2;
            a++;
        }
        return ans;
    }
};

